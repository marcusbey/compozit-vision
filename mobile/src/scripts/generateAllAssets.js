async function main() {
  const generator = new ExpandedAssetGenerator();
  const mode = process.argv[2] || 'all';
  const limit = process.argv[3] ? parseInt(process.argv[3]) : null;
  
  console.log(`üéØ Mode: ${mode.toUpperCase()}`);
  console.log(`‚ö†Ô∏è  NO TEXT Policy: All images will be generated without any text or labels`);
  
  try {
    switch (mode) {
      case 'priority1':
        console.log('üéØ Running PRIORITY 1 assets (NO TEXT)');
        await generator.generateByPriority(1, limit);
        break;
      case 'priority2':
        console.log('üìä Running PRIORITY 2 assets (NO TEXT)');
        await generator.generateByPriority(2, limit);
        break;
      case 'priority3':
        console.log('üé® Running PRIORITY 3 assets (NO TEXT)');
        await generator.generateByPriority(3, limit);
        break;
      case 'test':
        console.log('üß™ Running TEST mode (first 5 assets, NO TEXT)');
        await generator.generateByPriority(1, 5);
        break;
      case 'all':
      default:
        console.log('üåü Running ALL EXPANDED ASSETS (NO TEXT)');
        await generator.generateAllAssets();
        break;
    }
  } catch (error) {
    console.error('üí• Asset generation failed:', error);
    process.exit(1);
  }
}

